import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const rootDir = resolve(import.meta.dirname, "..");
const sqlDir = resolve(rootDir, "sql/sqlite");
const outputPath = resolve(rootDir, "src/cloudflare/schema.ts");

const migrations = readdirSync(sqlDir)
	.filter((filename) => /^\d+_.+\.sql$/.test(filename))
	.sort()
	.map((filename) => {
		const sqlPath = resolve(sqlDir, filename);
		const sql = readFileSync(sqlPath, "utf8").trimEnd();
		const match = filename.match(/^(\d+)_(.+)\.sql$/);
		if (!match) {
			throw new Error(`Invalid migration filename: ${filename}`);
		}

		return {
			version: Number(match[1]),
			name: match[2],
			sql,
		};
	});

const output = `// This file is generated from sql/sqlite/*.sql by:
//   pnpm run sync:schema
// It is also regenerated automatically by:
//   pnpm typecheck
//   pnpm build
// Do not edit this file directly. Edit sql/sqlite/*.sql instead.

import { cloudflareSqliteSchemaVersion } from "../versions";

export const requiredSchemaVersion = cloudflareSqliteSchemaVersion;

export const schemaMigrations = [
\t${migrations
	.map(
		(migration) => `{
\t\tversion: ${migration.version},
\t\tname: ${JSON.stringify(migration.name)},
\t\tsql: ${JSON.stringify(migration.sql)},
\t}`,
	)
	.join(",\n\t")}
] as const;
`;

writeFileSync(outputPath, output);
